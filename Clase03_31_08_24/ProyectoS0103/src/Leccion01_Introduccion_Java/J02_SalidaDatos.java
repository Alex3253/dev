package Leccion01_Introduccion_Java;

public class J02_SalidaDatos {
    public static void main(String[] args) {
        //Declaracion de variables
        String usuario;
        int edadUsuario;
        double estaturaUsuario;
        
        //Inicializacion de variables
        usuario = "Rita Medina";
        edadUsuario = 26;
        estaturaUsuario = 1.75;
        
        //Salidad de datos(sout + tab)
        System.out.println("Detalle de Uusario");
        System.out.println("------------------");
        System.out.println(usuario);
        System.out.println(edadUsuario);
        System.out.println(estaturaUsuario);
    }
    
}
